<?php
    // 简单数据类型

    // 定义四种整形数据类型
    $a1 = 110;
    $a2 = 0b110;
    $a3 = 0110;
    $a4 = 0x110;

    echo $a1, '~', $a2, '~', $a3, '~', $a4;

    // 浮点数
    $f1 = 1.23;
    $f2 = 1.23e10;
    $f3 = PHP_INI_MAX + 1;
    var_dump($f1, $f2, $f3);
    echo '<hr/>';
    
    // 浮点数比较
    $f4 = 0.7;
    $f5 = 2.1;
    $f6 = $f5 / 3;
    var_dump($f4 == $f6);
